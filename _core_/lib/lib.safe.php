function StopAttack($StrFiltKey, $StrFiltValue, $ArrFiltReq)
{
    if (is_array($StrFiltValue)) {
        $StrFiltValue = implode($StrFiltValue);
    }
    //如果出现风险输入，将其记录下来并终止运行
    if (preg_match("/" . $ArrFiltReq . "/is", $StrFiltValue) == 1) {
        $attackInput = "\n 【风险】 操作IP: ".getIp()." 操作时间: ".strftime("%Y-%m-%d %H:%M:%S")." 操作页面:".$_SERVER["PHP_SELF"].$_SERVER['REQUEST_URI']." 提交方式: ".$_SERVER["REQUEST_METHOD"]." 提交参数: ".$StrFiltKey." 提交数据: ".$StrFiltValue;
        writeLog($attackInput,APPROOT.'/data/log/safe.txt');
        echo 'Sorry,Input is no safe !!!';
        exit();
    }
}

